  <h1>Mois de Mai 2017</h1></br>

<h3>Ma journée 76</h3>
<p>Aujourd'hui Mardi 02 Mai 2017:</br>
Nous avons continué La "POO", j'ai regardé des vidéos sur Youtube puis sur un site et lu Openclassrooms. Et nous avons rencontré la nouvelle formatrice de Carcasonne qui reste avec nous tout le mois de mai.</p>

<h3>Ma journée 77</h3>
<p>Aujourd'hui Mercredi 03 Mai 2017:</br>
Toute la journée nous avons fait "Les mails en POO avec PHPMAILER" et "Générer un PDF".</br>Puis lire des PDF sur FPDF... Et en fin de journée nous avons rencontré les autres formateurs de la région, qui sont venu se présenter aprés leurs journée en réunion avec Monsieur Jean-Françon Kappes le directeur de la région.</p>

<h3>Ma journée 78</h3>
<p>Aujourd'hui Jeudi 04 Mai 2017:</br>
Le matin nous avons fait des "Carousels", j'ai téléchargé "FlexSilder". </br>L'après-midi nous avons corrigé les exercices de hier "PDF" et "PHPMAILER" avec la nouvelle formatrice.<p>

<h3>Ma journée 79</h3>
<p>Aujourd'hui Vendredi 05 Mai 2017:</br>
Journée avec la formatrice Elodie, elle ma bien expliqué les boucles dans des tableaux en JS. Nous avons fait des exercices d'algorithmie, première journée de ma formation ou j'apprécie d'être la du matin au soir, et aussi que la méthode de travail et présente pour avancer dans le métier qui sera normalement le mien l'année prochaine. Aujourd'hui vendredi 5 mai après presque 3 mois de formation, j'ai enfin compris le fonctionnement d'une boucle et d'un tableau. Merci à Elodie pour cette patience tout au long de la journée!!</p>

<h3>Ma journée 80</h3>
<p>Aujourd'hui Mardi 09 Mai 2017:</br>
Ce nous avons fait l'inoguration du "Fablab" de 9H à 10H30, nous avons écouté des gens qui ont expliqués leurs progrets pour améliorer les réseaux web en Occitanie. Puis de 14H à 15H, en petit groupe nous sommes allé écouter 3 personnes nous parlé du télètravail... Et pour finir la chef de projet de Occitanie Simplon ERN et autres formation, Madama Laure Soula et venue faire un point avec nous sur l'organisation de la formation etc...</p>

<h3>Ma journée 81</h3>
<p>Aujourd'hui Mercredi 10 Mai 2017:</br>
Journée "Algorithme" lecture et exercices sur <a href="http://pise.info/algo/codage.htm" style="text-decoration: none;" target="_blank">Pise.info</a> et j'ai finis les boucles, les exercices que la formatrice Elodie n'avait donner à faire.</br>A 17H nous sommes allé voir la réalitée virtuelle au Fablab.</p>


<h3>Ma journée 82</h3>
<p>Aujourd'hui Jeudi 11 Mai 2017:</br>
Ce matin j'ai continué l'Algorithme, lecture des cours plus exercices.</br> Puis correction des exercices : Boisson et Machine à café, qui nous à prit toute l'après-midi.</p>


<h3>Ma journée 83</h3>
<p>Aujourd'hui Vendredi 12 Mai 2017:</br>
Le matin une dame est venue nous présenter son projet sur les postures à avoir au travail sur l'alimentation etc...</br>L'après-midi nous avons fait un exercice: Récupérer les informations sur un visiteur sur notre server en PHP. En fin de journée nous avons fait la correction tout ensemble!!</p>

<h3>Ma journée 84</h3>
<p>Aujourd'hui Lundi 15 Mai 2017:</br>
Journée entretien individuel avec Monsieur Darras, Nicolas et Patrick. Nous avons fait le point sur la formation, mon ressenti etc...</br> En parallèle j'ai modifier mon CV et j'ai continué l'exercice de vendredi après-midi. Mais je n'ai pas fini comme je n'ai pas tout compris !!</p>

<h3>Ma journée 85</h3>
<p>Aujourd'hui Mardi 16 Mai 2017:</br>
Ce matin nous avons fait MyadminSQL les bases de données en "localhost" et sur "Server", puis après la pause nous sommes allé installer les tables, chaises et déballer les ordinateurs fixe pour les branchés au 2éme étage, dans notre nouvelle salle que nous devons avoir demain ou jeudi. Cette aprés-midi début de la correction des bases de données, puis j'ai fini la suite de SQL et en fin d'après-midi exercice sur "api-lastfm" que nous avons du "fork" et enseuite "clone" qur la console.</p>

<h3>Ma journée 86</h3>
<p>Aujourd'hui Mercredi 17 Mai 2017:</br>
Ce matin derniére partie et fin de l'exercice MyAdminSQL pour afficher l'heure. Après-midi L'API sur "Openclassrooms", sinon pas grand chose pour cette journée !!!</p>

<h3>Ma journée 87</h3>
<p>Aujourd'hui Jeudi 18 Mai 2017:</br>
Le matin nous avons parlé de l'organisation du planning pour les stages, les meetups, le travail à faire tout au long de la formation etc... Puis entre 12h et 14h nous avons assisté à la conférence sur le référencement sur Google à "Innoveum", conférence très intéréssante et construtive j'ai appris certaines choses qui sont importante quand t-on a un site internet. </br>Et en fin d'après-midi nous avons fait une simulation d'entretien d'embauche, nous étions divisé en deux groupes un côter "employeur" et de l'autre "demandeur".</p>

<h3>Ma journée 88</h3>
<p>Aujourd'hui Vendredi 19 Mai 2017:</br>
Ce matin nous avons renconté Monsieur Emmanuel Nagy "Expert dans la cyber sécuriter", toute la matinée il nous à expliqué se que c'était son métier le fonctionnement le but... Matinée très passionnante à l'écouter, puis le métier est très intéréssant. Puis le reste de la journée nous avons fait une intégration pour notre site de la promo !!</p>

<h3>Ma journée 89</h3>
<p>Aujourd'hui Lundi 22 Mai 2017:</br>
Journée dans notre nouvelle salle au 2éme étage, nous avons fait des nouveaux mots dans le "Lexique" mon mot étais "PDO". Puis nous avons tiré au sort les prénoms et les projets, il y a 3 groupes avec chaqu'un 1 ou 2 projets. Je suis sur le projet "ENEDIS" et "Jeu2piste" (presque terminé) pour Monsieur Lheureux avec trois autres personnes.</br>Cette après-midi nous les avons commencés le début des projets et tout au long de l'après-midi, aujourd'hui c'était plus pour faire les mockups, qui fait quoi, cahier des charges et tout se répartir...</p>

<h3>Ma journée 90</h3>
<p>Aujourd'hui Mardi 23 Mai 2017:</br>
Aujourd'hui nous avons continué les projets en groupes, nous avons répartir le travail et l'organisation du projet.</br>Journée entière sur "ENEDIS", et pour finir envoie de CV à Monsieur Darras pour Madame Soula. Pour mettre sur le sitePromo2017...</p>

<h3>Ma journée 91</h3>
<p>Aujourd'hui Mercredi 24 Mai 2017:</br>
Journée toujours sur les projets nous avons bien avancé sur "ENEDIS", J'ai modifiée mes pages je les ai mis en responsive ajouter les couleurs les tailles les mentions légales et le logo de l'entreprise</br>Ce soir en week-end pour 4 jours tranquille !!</p>


<h3>Ma journée 92</h3>
<p>Aujourd'hui Lundi 29 Mai 2017:</br>
Après un week-end de 4 jours reprise des projets, nous n'avons pas trop avancé dans notre projet "ENEDIS". Nous avons fait le "slide" avec les capture d'écran de se que l'on a déjà fait.</br>Nous sommes en attente des informations que le monsieur d'Enedis doit nous faire parvenir...</p>


<h3>Ma journée 93</h3>
<p>Aujourd'hui Mardi 30 Mai 2017:</br>
Projets encore, encore, encore et encore...Ce matin j'ai fait la page "Crédit" la où j'ai inscrit nos noms, prènoms et adresses e-mails, de chacune de nous. Sinon journée qui avance pas trop toujours pas d'information concernant le projet, du coup nous y allons à l'aveugle pour l'instant !!</br>Monsieur Lheureux à put télècharger son application Jeu2piste, il est content de notre travail et peut enfin démarcher des annonceurs et partenaires pour lancer cette application gratuite et super intérréssante !!</br></br>
Départ pour le MeetUp de Montpellier à 15h00 qui est sur "PHP GIT" qui ce déroule à école Epitech de 18h30-19h00 à 23h00 !!</p>


